package br.com.inteligate.depara.dados;

public class Horarios {
	private int nr_hor;
	private java.util.Date hrent;
	private java.util.Date hrsai;
	private int tpref_pri;
	private int tpref_sec;
	public int getNr_hor() {
		return nr_hor;
	}
	public void setNr_hor(int nr_hor) {
		this.nr_hor = nr_hor;
	}
	public java.util.Date getHrent() {
		return hrent;
	}
	public void setHrent(java.util.Date hrent) {
		this.hrent = hrent;
	}
	public java.util.Date getHrsai() {
		return hrsai;
	}
	public void setHrsai(java.util.Date hrsai) {
		this.hrsai = hrsai;
	}
	public int getTpref_pri() {
		return tpref_pri;
	}
	public void setTpref_pri(int tpref_pri) {
		this.tpref_pri = tpref_pri;
	}
	public int getTpref_sec() {
		return tpref_sec;
	}
	public void setTpref_sec(int tpref_sec) {
		this.tpref_sec = tpref_sec;
	}


}
